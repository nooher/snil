// codegen_js.ts — compiles a SNIL Program AST into equivalent, runnable
// JavaScript (ES2020, runnable via `node`). SNIL is its OWN language; JavaScript
// is a second compilation target alongside Python. The emitted JS, when executed,
// produces output IDENTICAL to the SNIL interpreter and the Python backend
// (see GRAMMAR.md + EXPECTED.md).
//
// This file MIRRORS codegen_python.ts in structure and quality:
//  • a runtime PRELUDE that replicates SNIL display + arithmetic + stdlib
//  • `leta hisabati`/etc → flatten stdlib module functions to bare names
//  • `leta "x"` file imports → INLINE the module's compiled code once
//    (dependency-ordered, deduped, cycle-safe) using the ModuleResolver
