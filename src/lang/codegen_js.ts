// Identifier names that conflict with JS keywords/reserved words/prelude globals
// are mangled. SNIL identifiers are Kiswahili but a few collide (e.g. `for`, `if`)
// or could shadow prelude helpers — mangle conservatively.
const JS_RESERVED = new Set([
  'break', 'case', 'catch', 'class', 'const', 'continue', 'debugger', 'default',
  'delete', 'do', 'else', 'export', 'extends', 'false', 'finally', 'for',
  'function', 'if', 'import', 'in', 'instanceof', 'new', 'null', 'return',
  'super', 'switch', 'this', 'throw', 'true', 'try', 'typeof', 'var', 'void',
  'while', 'with', 'yield', 'let', 'static', 'enum', 'await', 'async',
  'implements', 'interface', 'package', 'private', 'protected', 'public',
  'arguments', 'eval',
]);

function safeName(name: string): string {
  if (JS_RESERVED.has(name)) return name + '_';
  // Names that begin with `_` could collide with prelude helpers; SNIL user
  // identifiers cannot start with `_` per the lexer, so no extra guard needed.
  return name;
}

export function generateJS(program: Program, somaModuli?: import('./runtime').ModuleResolver): string {
  const out: string[] = [];

  function line(text: string, depth: number): void {
    out.push('  '.repeat(depth) + text);
  }

  function block(stmts: Stmt[], depth: number): void {
    for (const s of stmts) emitStmt(s, depth);
  }

  function emitStmt(s: Stmt, depth: number): void {
    switch (s.kind) {
      case 'VarDecl': {
        const n = s as VarDecl;
        // `weka` may re-declare an existing name (SNIL allows it). Use an
        // assign-or-declare pattern at top level via `var` to stay idempotent;
        // but inside functions/blocks `var` is hoisted, which is safe here too.
        line(`var ${safeName(n.name)} = ${expr(n.value)};`, depth);
        return;
      }
      case 'Assign': {
        const n = s as Assign;
        line(`${lvalue(n.target)} = ${expr(n.value)};`, depth);
        return;
      }
      case 'Print': {
        const n = s as Print;
        line(`_onyesha(${expr(n.value)});`, depth);
        return;
      }
      case 'Input': {
        const n = s as Input;
        line(`var ${safeName(n.name)} = _uliza(_str(${expr(n.prompt)}));`, depth);
        return;
      }
      case 'If': {
        const n = s as If;
        line(`if (_kweli(${expr(n.cond)})) {`, depth);
        block(n.then, depth + 1);
        if (n.otherwise !== null) {
          line('} else {', depth);
          block(n.otherwise, depth + 1);
          line('}', depth);
        } else {
          line('}', depth);
        }
        return;
      }
      case 'ForEach': {
        const n = s as ForEach;
        line(`for (const ${safeName(n.varName)} of ${expr(n.iterable)}) {`, depth);
        block(n.body, depth + 1);
        line('}', depth);
        return;
      }
      case 'ForRange': {
        const n = s as ForRange;
        // a..b INCLUSIVE both ends; step +1 up, -1 down (match interpreter).
        const v = safeName(n.varName);
        const from = `_from_${depth}_${v}`;
        const to = `_to_${depth}_${v}`;
        line(`{ const ${from} = ${expr(n.from)}, ${to} = ${expr(n.to)};`, depth);
        line(`if (${from} <= ${to}) {`, depth);
        line(`for (let ${v} = ${from}; ${v} <= ${to}; ${v}++) {`, depth + 1);
        block(n.body, depth + 2);
        line('}', depth + 1);
        line('} else {', depth);
        line(`for (let ${v} = ${from}; ${v} >= ${to}; ${v}--) {`, depth + 1);
        block(n.body, depth + 2);
        line('}', depth + 1);
        line('} }', depth);
        return;
      }
      case 'While': {
        const n = s as While;
        line(`while (_kweli(${expr(n.cond)})) {`, depth);
        block(n.body, depth + 1);
        line('}', depth);
        return;
      }
      case 'FuncDecl': {
        const n = s as FuncDecl;
        const params = n.params.map(safeName).join(', ');
        line(`function ${safeName(n.name)}(${params}) {`, depth);
        block(n.body, depth + 1);
        line('}', depth);
        return;
      }
      case 'Return': {
        const n = s as Return;
        line(n.value === null ? 'return;' : `return ${expr(n.value)};`, depth);
        return;
      }
      case 'Try': {
        const n = s as Try;
        line('try {', depth);
        block(n.body, depth + 1);
        line('} catch (_e) {', depth);
        block(n.handler, depth + 1);
        line('}', depth);
        return;
      }
      case 'Import': {
        const n = s as Import;
        if (n.isFile) {
          // File module: compiled statements are inlined ONCE at top level (see
          // gatherFileModules below), so the import site emits nothing.
          line(`// leta "${n.module}"`, depth);
          return;
        }
        // `leta X` flattens the stdlib module's functions into bare names —
        // matching the interpreter (so `jumla(...)` works after `leta hisabati`).
        const mod = STDLIB[n.module];
        if (mod) {
          for (const fn of Object.keys(mod)) line(`var ${safeName(fn)} = ${n.module}.${fn};`, depth);
        } else {
          line(`// leta ${n.module}`, depth);
        }
        return;
      }
      case 'ListAdd': {
        const n = s as ListAdd;
        line(`(${expr(n.list)}).push(${expr(n.item)});`, depth);
        return;
      }
      case 'ListRemove': {
        const n = s as ListRemove;
        line(`_ondoa(${expr(n.list)}, ${expr(n.item)});`, depth);
        return;
      }
      case 'FileWrite': {
        const n = s as FileWrite;
        line(`require("fs").writeFileSync(${expr(n.path)}, _str(${expr(n.data)}), "utf-8");`, depth);
        return;
      }
      case 'FileRead': {
        const n = s as FileRead;
        line(`var ${safeName(n.name)} = require("fs").readFileSync(${expr(n.path)}, "utf-8");`, depth);
        return;
      }
      case 'ExprStmt': {
        const n = s as ExprStmt;
        line(`${expr(n.expr)};`, depth);
        return;
      }
      default: {
        const _exhaustive: never = s;
        throw new Error(`SNIL codegen: aina ya sentensi haijatekelezwa: ${(_exhaustive as Stmt).kind}`);
      }
    }
  }

  function lvalue(target: Ident | Index | Member): string {
    switch (target.kind) {
      case 'Ident':
        return safeName(target.name);
      case 'Index':
        return `${expr(target.target)}[${expr(target.index)}]`;
      case 'Member':
        // SNIL dicts compile to JS objects → member access is subscript.
        return `${expr(target.target)}[${str(target.name)}]`;
    }
  }

  function str(s: string): string {
    return JSON.stringify(s);
  }

  function expr(e: Expr): string {
    switch (e.kind) {
      case 'NumberLit':
        return String(e.value);
      case 'StringLit':
        return str(e.value);
      case 'TemplateString': {
        // "Habari {jina}!" → ("Habari " + _str(jina) + "!"). Literal parts stay
        // plain strings; expr parts go through _str (same stringify as SNIL `+`).
        const pieces = e.parts.map((p) =>
          p.t === 'lit' ? str(p.value) : `_str(${expr(p.expr)})`,
        );
        return pieces.length ? `(${pieces.join(' + ')})` : '""';
      }
      case 'BoolLit':
        return e.value ? 'true' : 'false';
      case 'NullLit':
        return 'null';
      case 'ListLit':
        return `[${e.items.map(expr).join(', ')}]`;
      case 'DictLit':
        return `{${e.entries.map((en) => `${str(en.key)}: ${expr(en.value)}`).join(', ')}}`;
      case 'Ident':
        return safeName(e.name);
      case 'Binary':
        return binary(e as Binary);
      case 'Unary':
        return unary(e as Unary);
      case 'Call':
        return call(e as Call);
      case 'Index':
        return `${expr((e as Index).target)}[${expr((e as Index).index)}]`;
      case 'Member':
        return `${expr((e as Member).target)}[${str((e as Member).name)}]`;
      default: {
        const _exhaustive: never = e;
        throw new Error(`SNIL codegen: aina ya msemo haijatekelezwa: ${(_exhaustive as Expr).kind}`);
      }
    }
  }

  function binary(e: Binary): string {
    const l = expr(e.left);
    const r = expr(e.right);
    switch (e.op) {
      case '+':
        // SNIL `+`: string-concat if either side is text, else numeric add.
        return `_jumla(${l}, ${r})`;
      case '-': return `(${l} - ${r})`;
      case '*': return `(${l} * ${r})`;
      case '/': return `_gawanya(${l}, ${r})`;
      case '%': return `(${l} % ${r})`;
      case '==': return `_eq(${l}, ${r})`;
      case '!=': return `(!_eq(${l}, ${r}))`;
      case '<': return `(${l} < ${r})`;
      case '>': return `(${l} > ${r})`;
      case '<=': return `(${l} <= ${r})`;
      case '>=': return `(${l} >= ${r})`;
      // na/au short-circuit AND return a boolean (matches interpreter, which
      // returns isTruthy of the deciding operand). Wrap operands in _kweli.
      case 'na': return `(_kweli(${l}) ? _kweli(${r}) : false)`;
      case 'au': return `(_kweli(${l}) ? true : _kweli(${r}))`;
    }
  }

  function unary(e: Unary): string {
    const v = expr(e.operand);
    if (e.op === 'sio') return `(!_kweli(${v}))`;
    return `(-${v})`;
  }

  function call(e: Call): string {
    const args = e.args.map(expr).join(', ');
    // Global builtins available without `leta`.
    if (e.callee === 'idadi') return `_idadi(${args})`;
    // `maandishi` is BOTH a global builtin and a module name; the module object
    // shadows the builtin, so route the global display-string builtin to its
    // underscored prelude function (like _idadi). Mirrors the Python backend.
    if (e.callee === 'maandishi') return `_maandishi(${args})`;
    return `${safeName(e.callee)}(${args})`;
  }

  // ── gather transitively-imported file modules ──
  // DFS from main, DEDUPED by module name, CYCLE-SAFE (don't recurse into a
  // module already on the visiting stack), emitted in dependency order
  // (imported-before-importer). Each module body is compiled exactly ONCE so its
  // top-level kazi/weka become global names — exactly the bare-name exposure the
  // interpreter produces. Mirrors codegen_python.ts.
  function fileImportsOf(body: Stmt[]): Import[] {
    const found: Import[] = [];
    for (const s of body) {
      if (s.kind === 'Import' && (s as Import).isFile) found.push(s as Import);
    }
    return found;
  }

  const moduleOrder: { name: string; body: Stmt[] }[] = [];
  const emitted = new Set<string>();
  const visiting = new Set<string>();

  function visitModule(imp: Import): void {
    const name = imp.module;
    if (emitted.has(name)) return;
    if (visiting.has(name)) return;
    if (!somaModuli) {
      throw new SnilError(
        `Moduli "${name}" haijapatikana.`, imp.line, 'kutekeleza',
        'mazingira haya hayana kisuluhishi cha moduli (somaModuli)',
      );
    }
    const source = somaModuli(name);
    if (source == null) {
      throw new SnilError(`Moduli "${name}" haijapatikana.`, imp.line, 'kutekeleza');
    }
    const modProgram = parseTokens(tokenize(source));
    visiting.add(name);
    for (const dep of fileImportsOf(modProgram.body)) visitModule(dep);
    visiting.delete(name);
    if (!emitted.has(name)) {
      emitted.add(name);
      moduleOrder.push({ name, body: modProgram.body });
    }
  }

  for (const imp of fileImportsOf(program.body)) visitModule(imp);

  // ── assemble ──
  // 1) inlined file-modules (dependency order), each emitted ONCE at top level.
  for (const mod of moduleOrder) {
    out.push(`// ── moduli "${mod.name}" (imeingizwa moja kwa moja) ──`);
    for (const s of mod.body) emitStmt(s, 0);
  }
  // 2) the main program body.
  for (const s of program.body) emitStmt(s, 0);
  // 3) flush collected output once at the end.
  out.push('_flush();');

  return PRELUDE + '\n' + out.join('\n') + '\n';
}
